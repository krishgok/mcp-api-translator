export function indexHttpTs(name: string): string {
  return [
    GENERATED_BANNER,
    "",
    'import { randomUUID } from "node:crypto";',
    'import { createServer as createHttpServer } from "node:http";',
    'import { StreamableHTTPServerTransport } from "@modelcontextprotocol/sdk/server/streamableHttp.js";',
    'import { createServer } from "./server.js";',
    'import { log } from "./logger.js";',
    "",
    "const port = Number(process.env.PORT ?? 3000);",
    "",
    "// DNS-rebinding protection: only accept requests whose Host header is in this allowlist, so a",
    "// malicious web page cannot rebind to localhost and drive this server. Override for non-local",
    "// deployments via MCP_ALLOWED_HOSTS (comma-separated host[:port] values).",
    'const defaultHosts = "127.0.0.1:" + port + ",localhost:" + port;',
    "const allowedHostList = (process.env.MCP_ALLOWED_HOSTS ?? defaultHosts)",
    '  .split(",")',
    "  .map((h) => h.trim())",
    "  .filter(Boolean);",
    "",
    "// Stateless Streamable HTTP: a fresh server+transport per request.",
    "createHttpServer(async (req, res) => {",
    "  const start = Date.now();",
    '  const header = req.headers["x-request-id"];',
    "  const requestId = (Array.isArray(header) ? header[0] : header) || randomUUID();",
    "  let logged = false;",
    "  const logRequest = (): void => {",
    "    if (logged) return;",
    "    logged = true;",
    '    log.info("http request", {',
    "      requestId,",
    "      method: req.method,",
    "      path: req.url,",
    "      status: res.statusCode,",
    "      durationMs: Date.now() - start,",
    "    });",
    "  };",
    '  if (!req.url || !req.url.startsWith("/mcp")) {',
    "    res.statusCode = 404;",
    '    res.end("Not found");',
    '    log.debug("http request (not found)", { requestId, method: req.method, path: req.url, status: 404 });',
    "    return;",
    "  }",
    '  res.on("finish", logRequest);',
    "  const server = createServer();",
    "  const transport = new StreamableHTTPServerTransport({",
    "    sessionIdGenerator: undefined,",
    "    enableDnsRebindingProtection: true,",
    "    allowedHosts: allowedHostList,",
    "  });",
    '  res.on("close", () => {',
    "    logRequest();",
    "    void transport.close();",
    "    void server.close();",
    "  });",
    "  await server.connect(transport);",
    "  await transport.handleRequest(req, res);",
    "}).listen(port, () => {",
    `  log.info(${JSON.stringify(name + " MCP server on http://localhost:")} + port + "/mcp");`,
    "});",
    "",
  ].join("\n");
}

export function envExample(
  baseUrl: string,
  schemes: SecurityScheme[],
  sources: EmitSource[] = [],
): string {
  const lines = ["# Environment for the generated MCP server.", ""];
  lines.push("# Base URL of the upstream API.");
  lines.push(`API_BASE_URL=${baseUrl || "https://api.example.com"}`);
  if (schemes.length > 0) {
    lines.push("");
    lines.push("# Credentials (read at runtime; never commit real values).");
    for (const s of schemes) {
      for (const v of s.envVars) lines.push(`${v}=`);
    }
  }
  if (sources.length > 1) {
    lines.push("");
    lines.push("# Per-API overrides (this server aggregates several APIs). A namespaced var");
    lines.push("# (<NAMESPACE>_<VAR>) wins over the bare var above for that API's tools.");
    for (const s of sources) {
      lines.push(`# ${commentSafe(s.title)}:`);
      lines.push(`${s.namespace}_API_BASE_URL=${s.baseUrl}`);
      for (const v of s.credentialVars) lines.push(`${s.namespace}_${v}=`);
    }
  }
  lines.push("");
  lines.push("# Logging (JSON lines on stderr when not a TTY — cloud log agents parse them).");
  lines.push("# LOG_LEVEL=info");
  lines.push("# LOG_FORMAT=json");
  lines.push("");
  return lines.join("\n");
}

export function serverJson(name: string, version: string, description: string): string {
  // Minimal MCP Registry-style manifest the user can adapt before publishing.
  const manifest = {
    $schema: "https://static.modelcontextprotocol.io/schemas/2025-09-29/server.schema.json",
    name: `io.github.OWNER/${name}`,
    description,
    version,
    packages: [
      {
        registryType: "npm",
        identifier: name,
        version,
        transport: { type: "stdio" },
      },
    ],
  };
  return JSON.stringify(manifest, null, 2) + "\n";
}

