export function configTs(defaultBaseUrl: string, sources: EmitSource[] = []): string {
  // Per-source default base URLs (namespace -> URL); empty declared servers are omitted so the
  // resolver falls through to the global default.
  const sourceMap = Object.fromEntries(
    sources.filter((s) => s.baseUrl).map((s) => [s.namespace, s.baseUrl]),
  );
  return [
    GENERATED_BANNER,
    "",
    `const defaultBaseUrl = ${JSON.stringify(defaultBaseUrl)};`,
    "",
    "// Default base URL per aggregated source API (env namespace -> the spec's first server).",
    `export const sources: Record<string, string> = ${JSON.stringify(sourceMap, null, 2)};`,
    "",
    "// Resolution order matches `serve`: <NS>_API_BASE_URL, then API_BASE_URL, then the source's",
    "// own declared server, then the project-wide default.",
    "export function resolveBaseUrl(ns?: string): string {",
    "  return (",
    '    (ns ? process.env[ns + "_API_BASE_URL"] : undefined) ??',
    "    process.env.API_BASE_URL ??",
    "    (ns ? sources[ns] : undefined) ??",
    "    defaultBaseUrl",
    "  );",
    "}",
    "",
    "export const config = {",
    "  baseUrl: process.env.API_BASE_URL ?? defaultBaseUrl,",
    "};",
    "",
  ].join("\n");
}

/** Collapse whitespace/newlines so a spec-derived value can't break out of a `//` comment. */
function commentSafe(value: string): string {
  return value.replace(/\s+/g, " ").trim();
}

function authBlock(scheme: SecurityScheme): string[] {
  const lines: string[] = [];
  const guard = `  if (security.includes(${JSON.stringify(scheme.name)})) {`;
  const label = commentSafe(
    `${scheme.name} (${scheme.type}${scheme.scheme ? "/" + scheme.scheme : ""})`,
  );
  lines.push(`  // ${label}`);
  lines.push(guard);
  if (scheme.type === "apiKey") {
    const env = scheme.envVars[0]!;
    const param = scheme.paramName ?? "X-API-Key";
    lines.push(`    const value = readEnv(ns, ${JSON.stringify(env)});`);
    lines.push("    if (value) {");
    if (scheme.in === "query") {
      lines.push(`      url.searchParams.set(${JSON.stringify(param)}, value);`);
    } else if (scheme.in === "cookie") {
      lines.push(
        `      headers["cookie"] = (headers["cookie"] ? headers["cookie"] + "; " : "") + ${JSON.stringify(
          param + "=",
        )} + value;`,
      );
    } else {
      lines.push(`      headers[${JSON.stringify(param)}] = value;`);
    }
    lines.push("    }");
  } else if (scheme.type === "http" && scheme.scheme?.toLowerCase() === "basic") {
    const [u, p] = scheme.envVars;
    lines.push(`    const user = readEnv(ns, ${JSON.stringify(u)});`);
    lines.push(`    const pass = readEnv(ns, ${JSON.stringify(p)});`);
    lines.push("    if (user && pass) {");
    lines.push(
      '      headers["authorization"] = "Basic " + Buffer.from(user + ":" + pass).toString("base64");',
    );
    lines.push("    }");
  } else if (scheme.tokenUrl && scheme.grant === "refresh_token") {
    // oauth2 refresh-token grant: exchange a pre-obtained refresh token for access tokens; a
    // pre-obtained bearer in the trailing env var still works as a fallback.
    const [id, secret, refresh, fallback] = scheme.envVars;
    lines.push(`    const clientId = readEnv(ns, ${JSON.stringify(id)});`);
    lines.push(`    const clientSecret = readEnv(ns, ${JSON.stringify(secret)});`);
    lines.push(`    const refreshToken = readEnv(ns, ${JSON.stringify(refresh)});`);
    lines.push("    if (clientId && refreshToken) {");
    lines.push(
      `      const token = await getRefreshGrantToken(${JSON.stringify(scheme.tokenUrl)}, clientId, clientSecret, refreshToken);`,
    );
    lines.push('      headers["authorization"] = "Bearer " + token;');
    lines.push("    } else {");
    lines.push(`      const value = readEnv(ns, ${JSON.stringify(fallback)});`);
    lines.push('      if (value) headers["authorization"] = "Bearer " + value;');
    lines.push("    }");
  } else if (scheme.tokenUrl) {
    // oauth2 client-credentials: exchange a client id/secret for a bearer token.
    const [id, secret] = scheme.envVars;
    lines.push(`    const clientId = readEnv(ns, ${JSON.stringify(id)});`);
    lines.push(`    const clientSecret = readEnv(ns, ${JSON.stringify(secret)});`);
    lines.push("    if (clientId && clientSecret) {");
    lines.push(
      `      const token = await getClientCredentialsToken(${JSON.stringify(scheme.tokenUrl)}, clientId, clientSecret);`,
    );
    lines.push('      headers["authorization"] = "Bearer " + token;');
    lines.push("    }");
  } else {
    // http bearer, oauth2, openIdConnect -> pre-obtained token.
    const env = scheme.envVars[0]!;
    lines.push(`    const value = readEnv(ns, ${JSON.stringify(env)});`);
    lines.push('    if (value) headers["authorization"] = "Bearer " + value;');
  }
  lines.push("  }");
  return lines;
}

/** Emitted client-credentials token fetch, only when such a scheme is present. */
function oauthCcHelperTs(): string[] {
  return [
    "async function getClientCredentialsToken(",
    "  tokenUrl: string,",
    "  clientId: string,",
    "  clientSecret: string,",
    "): Promise<string> {",
    '    const key = tokenUrl + "|" + clientId;',
    "    const hit = _tokenCache.get(key);",
    "    if (hit && hit.expiresAt > Date.now()) return hit.token;",
    "    const body = new URLSearchParams({",
    '      grant_type: "client_credentials",',
    "      client_id: clientId,",
    "      client_secret: clientSecret,",
    "    }).toString();",
    "    const res = await fetch(tokenUrl, {",
    '      method: "POST",',
    '      headers: { "content-type": "application/x-www-form-urlencoded" },',
    "      body,",
    "    });",
    "    const text = await res.text();",
    "    if (!res.ok)",
    '      throw new Error("OAuth token request failed: HTTP " + res.status + " " + text.slice(0, 300));',
    "    const json = JSON.parse(text) as { access_token?: string; expires_in?: number };",
    '    if (!json.access_token) throw new Error("OAuth token response had no access_token");',
    '    const ttl = typeof json.expires_in === "number" ? json.expires_in : 3600;',
    "    _tokenCache.set(key, { token: json.access_token, expiresAt: Date.now() + ttl * 1000 - 30000 });",
    "    return json.access_token;",
    "}",
    "",
  ];
}

/** Emitted refresh-token grant exchange, only when such a scheme is present. */
function oauthRefreshHelperTs(): string[] {
  return [
    "// Rotated refresh tokens live only in memory; re-supply via env after a restart if your",
    "// provider invalidates the old token on each exchange.",
    "const _refreshOverrides = new Map<string, string>();",
    "",
    "async function getRefreshGrantToken(",
    "  tokenUrl: string,",
    "  clientId: string,",
    "  clientSecret: string | undefined,",
    "  refreshToken: string,",
    "): Promise<string> {",
    '    const key = tokenUrl + "|" + clientId + "|" + refreshToken;',
    "    const hit = _tokenCache.get(key);",
    "    if (hit && hit.expiresAt > Date.now()) return hit.token;",
    "    const fields: Record<string, string> = {",
    '      grant_type: "refresh_token",',
    "      refresh_token: _refreshOverrides.get(key) ?? refreshToken,",
    "      client_id: clientId,",
    "    };",
    "    if (clientSecret) fields.client_secret = clientSecret;",
    "    const body = new URLSearchParams(fields).toString();",
    "    const res = await fetch(tokenUrl, {",
    '      method: "POST",',
    '      headers: { "content-type": "application/x-www-form-urlencoded" },',
    "      body,",
    "    });",
    "    const text = await res.text();",
    "    if (!res.ok)",
    '      throw new Error("OAuth token request failed: HTTP " + res.status + " " + text.slice(0, 300));',
    "    const json = JSON.parse(text) as {",
    "      access_token?: string;",
    "      expires_in?: number;",
    "      refresh_token?: string;",
    "    };",
    '    if (!json.access_token) throw new Error("OAuth token response had no access_token");',
    "    if (json.refresh_token) _refreshOverrides.set(key, json.refresh_token);",
    '    const ttl = typeof json.expires_in === "number" ? json.expires_in : 3600;',
    "    _tokenCache.set(key, { token: json.access_token, expiresAt: Date.now() + ttl * 1000 - 30000 });",
    "    return json.access_token;",
    "}",
    "",
  ];
}

export function authTs(schemes: SecurityScheme[]): string {
  const body = schemes.flatMap(authBlock);
  const hasOAuth = schemes.some((s) => s.tokenUrl);
  const hasCc = schemes.some((s) => s.tokenUrl && s.grant !== "refresh_token");
  const hasRefresh = schemes.some((s) => s.tokenUrl && s.grant === "refresh_token");
  const helpers = hasOAuth
    ? [
        "const _tokenCache = new Map<string, { token: string; expiresAt: number }>();",
        "",
        ...(hasCc ? oauthCcHelperTs() : []),
        ...(hasRefresh ? oauthRefreshHelperTs() : []),
      ]
    : [];
  const signature = hasOAuth
    ? [
        "export async function applyAuth(",
        "  headers: Record<string, string>,",
        "  url: URL,",
        "  security: string[],",
        "  ns?: string,",
        "): Promise<void> {",
      ]
    : [
        "export function applyAuth(",
        "  headers: Record<string, string>,",
        "  url: URL,",
        "  security: string[],",
        "  ns?: string,",
        "): void {",
      ];
  return [
    GENERATED_BANNER,
    "",
    "// Injects credentials read from the environment. Secrets are never embedded.",
    "// `ns` is the calling tool's source namespace: when several APIs are aggregated, the",
    "// namespaced env var (<NS>_<VAR>) wins over the bare one so credentials don't collide.",
    "function readEnv(ns: string | undefined, name: string): string | undefined {",
    '  return (ns ? process.env[ns + "_" + name] : undefined) ?? process.env[name];',
    "}",
    "",
    ...helpers,
    ...signature,
    ...(body.length > 0 ? body : ["  // No security schemes detected on the source API."]),
    "}",
    "",
  ].join("\n");
}

export function httpClientTs(): string {
  return [
    GENERATED_BANNER,
    "",
    'import { resolveBaseUrl } from "../config.js";',
    'import { applyAuth } from "../auth.js";',
    'import { log } from "../logger.js";',
    "",
    "export interface RequestPlan {",
    "  method: string;",
    "  pathTemplate: string;",
    "  pathParams: string[];",
    "  queryParams: string[];",
    "  headerParams: string[];",
    "  cookieParams: string[];",
    "  bodyParam: string | null;",
    "  contentType: string | null;",
    "  security: string[];",
    "}",
    "",
    "function joinUrl(base: string, path: string): string {",
    '  if (!base) throw new Error("API base URL is not set. Set API_BASE_URL in the environment.");',
    '  return base.replace(/\\/+$/, "") + "/" + path.replace(/^\\/+/, "");',
    "}",
    "",
    "// `sourceNamespace` scopes env lookups (base URL + credentials) to the tool's source API",
    "// when several APIs are aggregated; omitted (legacy tool files), the bare vars are used.",
    "export async function callOperation(",
    "  plan: RequestPlan,",
    "  args: Record<string, unknown>,",
    "  sourceNamespace?: string,",
    "): Promise<string> {",
    "  let path = plan.pathTemplate;",
    "  for (const name of plan.pathParams) {",
    "    const value = args[name];",
    '    path = path.split("{" + name + "}").join(encodeURIComponent(String(value ?? "")));',
    "  }",
    "  const url = new URL(joinUrl(resolveBaseUrl(sourceNamespace), path));",
    "  for (const name of plan.queryParams) {",
    "    const value = args[name];",
    "    if (value === undefined || value === null) continue;",
    "    // Array query values are repeated (?k=1&k=2), the OpenAPI default (form/explode).",
    "    if (Array.isArray(value)) {",
    "      for (const v of value) {",
    "        if (v !== undefined && v !== null) url.searchParams.append(name, String(v));",
    "      }",
    "    } else {",
    "      url.searchParams.set(name, String(value));",
    "    }",
    "  }",
    "  const headers: Record<string, string> = {};",
    "  for (const name of plan.headerParams) {",
    "    const value = args[name];",
    "    if (value === undefined || value === null) continue;",
    '    headers[name] = Array.isArray(value) ? value.map(String).join(",") : String(value);',
    "  }",
    "  const cookies: string[] = [];",
    "  for (const name of plan.cookieParams) {",
    "    const value = args[name];",
    "    if (value !== undefined && value !== null) {",
    '      cookies.push(name + "=" + encodeURIComponent(String(value)));',
    "    }",
    "  }",
    "  if (cookies.length > 0) {",
    '    headers["cookie"] = (headers["cookie"] ? headers["cookie"] + "; " : "") + cookies.join("; ");',
    "  }",
    "  let body: string | undefined;",
    "  if (plan.bodyParam && args[plan.bodyParam] !== undefined) {",
    "    const raw = args[plan.bodyParam];",
    '    if (plan.contentType && plan.contentType.indexOf("json") >= 0) {',
    '      headers["content-type"] = "application/json";',
    "      body = JSON.stringify(raw);",
    "    } else {",
    '      if (plan.contentType) headers["content-type"] = plan.contentType;',
    '      body = typeof raw === "string" ? raw : JSON.stringify(raw);',
    "    }",
    "  }",
    "  await applyAuth(headers, url, plan.security, sourceNamespace);",
    "  const start = Date.now();",
    "  const response = await fetch(url, { method: plan.method, headers, body });",
    "  const text = await response.text();",
    "  // origin + pathname only: query strings can carry apiKey credentials, headers always might.",
    '  log.debug("upstream response", {',
    "    method: plan.method,",
    "    url: url.origin + url.pathname,",
    "    status: response.status,",
    "    durationMs: Date.now() - start,",
    "  });",
    "  if (!response.ok) {",
    '    throw new Error("HTTP " + response.status + " " + response.statusText + ": " + text.slice(0, 800));',
    "  }",
    "  return text;",
    "}",
    "",
  ].join("\n");
}

