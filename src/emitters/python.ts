export interface PyReadmeData {
  serverName: string;
  pkg: string;
  apiTitle: string;
  toolCount: number;
  schemes: SecurityScheme[];
}

export function readmePy(data: PyReadmeData): string {
  const envLines = data.schemes.flatMap((s) => s.envVars).map((v) => "- `" + v + "`");
  return [
    `# ${data.serverName}`,
    "",
    `A Python MCP server for **${data.apiTitle}**, generated by ${GENERATOR_NAME}.`,
    `It exposes ${data.toolCount} tool(s) that call the upstream API directly.`,
    "",
    "## Quickstart",
    "",
    "```bash",
    "python -m venv .venv && source .venv/bin/activate",
    "pip install -e .",
    "cp .env.example .env   # then fill in values",
    `python -m ${data.pkg}   # runs over stdio`,
    "```",
    "",
    "## Configuration",
    "",
    "- `API_BASE_URL` — base URL of the upstream API.",
    ...(envLines.length > 0 ? ["", "Credentials:", ...envLines] : []),
    "",
    "When this server aggregates several APIs, each one also reads namespaced overrides —",
    "`<NAMESPACE>_API_BASE_URL` and `<NAMESPACE>_<VAR>` win over the bare variables for that",
    "API's tools (see `.env.example` for the exact names).",
    "",
    "## Connecting a client",
    "",
    "```json",
    "{",
    '  "mcpServers": {',
    `    "${data.serverName}": {`,
    '      "command": "python",',
    `      "args": ["-m", "${data.pkg}"],`,
    '      "env": { "API_BASE_URL": "https://api.example.com" }',
    "    }",
    "  }",
    "}",
    "```",
    "",
  ].join("\n");
}
