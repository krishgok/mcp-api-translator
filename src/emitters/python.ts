export function serverPy(name: string): string {
  return [
    BANNER,
    "import mcp.types as types",
    "from mcp.server.lowlevel import Server",
    "",
    "from .http_client import call_operation",
    "from .tools import TOOLS",
    "",
    `server = Server(${JSON.stringify(name)})`,
    "",
    "",
    "@server.list_tools()",
    "async def list_tools():",
    "    return [",
    '        types.Tool(name=t["name"], description=t["description"], inputSchema=t["inputSchema"])',
    "        for t in TOOLS",
    "    ]",
    "",
    "",
    "@server.call_tool()",
    "async def call_tool(name, arguments):",
    '    tool = next((t for t in TOOLS if t["name"] == name), None)',
    "    if tool is None:",
    '        return [types.TextContent(type="text", text="Unknown tool: " + name)]',
    "    try:",
    '        text = call_operation(tool["plan"], arguments or {}, tool.get("sourceNamespace"))',
    '        return [types.TextContent(type="text", text=text)]',
    "    except Exception as error:  # noqa: BLE001",
    '        return [types.TextContent(type="text", text="Error: " + str(error))]',
    "",
  ].join("\n");
}

