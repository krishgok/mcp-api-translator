/**
 * String templates for a generated **Python** MCP-server project.
 *
 * Mirrors the TypeScript emitter (`templates.ts`) one-for-one so behavior matches: low-level MCP
 * Python SDK, raw JSON-Schema tool inputs (no lossy conversion), env-based auth, stdlib `urllib`
 * for HTTP (the only third-party dependency is `mcp`). Spec-derived strings are JSON-encoded — a
 * JSON string literal is also a valid Python string literal — and comments are newline-stripped.
 */
import type { JsonSchema, SecurityScheme } from "../ir/model.js";
import type { EmitSource } from "./templates.js";
import { GENERATOR_NAME, GENERATOR_VERSION } from "../version.js";

const BANNER = `# Generated by ${GENERATOR_NAME} v${GENERATOR_VERSION}. Safe to edit.`;

/** A serializable tool record written to tools.json and loaded at runtime. */
export interface PyTool {
  name: string;
  description: string;
  inputSchema: JsonSchema;
  plan: unknown;
  /** Env namespace of the tool's source API; absent on legacy records (bare vars only). */
  sourceNamespace?: string;
}

/** Collapse whitespace so a spec-derived value can't break out of a `#` comment. */
function commentSafe(value: string): string {
  return value.replace(/\s+/g, " ").trim();
}

/** Python package name: importable, lower_snake, never leading-digit. */
export function toPackageModule(serverName: string): string {
  let mod = serverName
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "_")
    .replace(/^_+|_+$/g, "");
  if (mod.length === 0) mod = "api_mcp";
  if (/^[0-9]/.test(mod)) mod = `pkg_${mod}`;
  return mod;
}

